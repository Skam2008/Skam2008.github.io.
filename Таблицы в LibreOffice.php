        <h1>Создание и работа с таблицами в LibreOffice</h1>
        <h2>Вот пошаговое руководство по созданию и работе с таблицами в LibreOffice Calc:</h2>

        <h3>1. Создание новой таблицы:</h3>
        <ul>
            <li>Запустите LibreOffice.</li>
            <li>Выберите "Calc" из списка приложений. Это откроет новый пустой лист электронных таблиц.</li>
        </ul>

        <h3>2. Ввод данных:</h3>
        <ul>
            <li>Просто щелкните на ячейке и начните вводить данные. Вы можете вводить текст, числа, даты и формулы. Calc автоматически определяет тип данных.</li>
            <li>Для перехода между ячейками используйте клавиши со стрелками на клавиатуре или кликайте мышью.</li>
        </ul>

        <h3>3. Форматирование ячеек:</h3>
        <ul>
            <li>Выделение ячеек: Выделите одну или несколько ячеек, кликнув на них мышью или используя клавиши Shift и стрелки.</li>
            <li>Изменение шрифта: На панели инструментов или в меню "Формат" -> "Стили и форматирование" вы можете изменить шрифт, размер, стиль и цвет текста.</li>
            <li>Выравнивание текста: Выберите выравнивание текста (по левому, центру или правому краю, по вертикали).</li>
            <li>Изменение ширины столбцов и высоты строк: Наведите указатель мыши на границу между заголовками столбцов или строк, пока он не изменит форму на двунаправленную стрелку. Затем перетащите границу, чтобы изменить размер. Можно также указать точный размер в меню "Формат" -> "Столбец" или "Строка".</li>
            <li>Заливка ячеек: Выберите цвет заливки ячеек на панели инструментов.</li>
            <li>Границы ячеек: Настройте стиль и цвет границ ячеек.</li>
            <li>Форматирование чисел: Вы можете изменить формат отображения чисел (валюта, процент, дата и т.д.) в меню "Формат" -> "Ячейки".</li>
        </ul>

        <h3>4. Работа с формулами:</h3>
        <ul>
            <li>Ввод формул: Начните формулу со знака равенства (=). Например: =SUM(A1:A10) суммирует значения в ячейках от A1 до A10.</li>
            <li>Ссылки на ячейки: В формулах используются ссылки на ячейки (например, A1, B2).</li>
            <li>Автозаполнение: После ввода формулы в первую ячейку, можно протянуть маркер автозаполнения (маленький квадратик в правом нижнем углу ячейки) вниз или вправо, чтобы скопировать формулу в другие ячейки.</li>
            <li>Функции: LibreOffice Calc имеет множество встроенных функций, которые можно найти через меню "Вставка" -> "Функция".</li>
        </ul>

        <h3>5. Вставка и удаление строк и столбцов:</h3>
        <ul>
            <li>Вставка: Щелкните правой кнопкой мыши на заголовок строки или столбца, выберите "Вставить".</li>
            <li>Удаление: Щелкните правой кнопкой мыши на заголовок строки или столбца, выберите "Удалить".</li>
        </ul>

        <h3>6. Сортировка и фильтрация:</h3>
        <ul>
            <li>Сортировка: Выделите данные, которые нужно отсортировать, и выберите "Данные" -> "Сортировка".</li>
            <li>Фильтрация: Выделите данные и выберите "Данные" -> "Фильтр".</li>
        </ul>

        <h3>7. Сохранение таблицы:</h3>
        <ul>
            <li>Выберите "Файл" -> "Сохранить" и укажите имя и местоположение файла. Рекомендуется использовать формат .xlsx (совместимость с Microsoft Excel) или .ods (родной формат LibreOffice).</li>
        </ul>

        <h3>8. Дополнительные возможности:</h3>
        <ul>
            <li>Диаграммы: Создавайте различные диаграммы на основе данных в таблице.</li>
            <li>Графики: Стройте графики функций и данных.</li>
            <li>Макросы: Автоматизируйте сложные задачи с помощью макросов.</li>
            <li>Работа с базами данных: Подключайтесь к базам данных и импортируйте данные.</li>
        </ul>

        <p>Это лишь краткий обзор возможностей LibreOffice Calc. Рекомендуется изучить встроенную справку программы или посмотреть обучающие видео для более глубокого понимания функций и возможностей.</p>
